# Operadores Comparativos

variavel = 5

if variavel == 5:
    print("Os valores são iguais")
    
if variavel != 7:
    print("O valor da variável não é igual a 7")
    
if variavel > 2:
    print("O valor da variável é maior que 2")
    
if variavel >= 5:
    print("O valor da variável é maior ou igaul a 5")
    
if variavel < 7:
    print("O valor da variável é menor que 7")
    
if variavel <= 5:
    print("O valor da variavel é menor ou igual a 5")